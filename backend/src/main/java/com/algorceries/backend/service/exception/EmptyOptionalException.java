package com.algorceries.backend.service.exception;

public class EmptyOptionalException extends Exception {

    private final Class<?> clazz;

    // /////////////////////////////////////////////////////////////////////////
    // Init
    // /////////////////////////////////////////////////////////////////////////

    public EmptyOptionalException(Class<?> clazz) {
        this.clazz = clazz;
    }

    // /////////////////////////////////////////////////////////////////////////
    // Getters and Setters
    // /////////////////////////////////////////////////////////////////////////

    public Class<?> getClazz() {
        return clazz;
    }
}
